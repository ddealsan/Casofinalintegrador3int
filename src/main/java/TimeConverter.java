public class TimeConverter {
    public double convertEarthToPlanetTime(double earthSeconds) {
        // Implementación de la conversión de tiempo de la Tierra al nuevo planeta
        return earthSeconds * 1.5; // Ejemplo simple de conversión
    }

    public double convertPlanetToEarthTime(double planetSeconds) {
        // Implementación de la conversión de tiempo del nuevo planeta a la Tierra
        return planetSeconds / 1.5; // Ejemplo simple de conversión
    }
}